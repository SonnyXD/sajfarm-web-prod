                    <div class="card">
                        <div class="card-body">
                            <h3 class="mb-4">Gestiune</h3>
                            <p>In <b>gestiune</b> se pot regasi toate locatiile ce pot avea stocuri de produse (farmacie, stoc 3, substatii).
                              Fiecare gestiune are aceleasi subgestiuni, spre exemplu: medicamente, materiale sanitare, dezinfectanti etc, iar la cerere, pot fi adaugate gestiuni si chiar subgestiuni noi.
                            </p>
                            <p>Pentru a naviga pe o anumita gestiune, trebuie apasat pe butonul GESTIUNE din sidebar-ul din stanga, iar pentru a naviga pe o anumita subgestiune se procedeaza la fel, deasupra fiecarui tabel din gestiunea aleasa,
                              vor aparea toate subgestiunile.
                            </p>
                            <p>In fiecare gestiune se afla tabelul fiecarei subgestiuni, unde pot fi gasite produsele din nomenclator. Pentru a vizualiza toate detaliile
                              pozitiilor unui produs din gestiunea aleasa, se apasa click pe produsul din tabel. Este implementata de asemenea si un search bar dinamic, care va poate ajuta
                              in gasirea produselor. Cantitatea totala reprezinta suma tuturor pozitiilor pe care produsul le are. Spre exemplu:
                              Daca Algocalmin 500mg are 2 pozitii diferite (deci cu detalii diferite), sunt preluate ambele cantitati si insumate,
                              rezultatul fiind cantitatea totala disponibila.
                            </p>
                            <p>Deoarece farmacia si stocul 3 sunt gestiunile principale, am adaugat conceptul de "cantitate minima". La "cantitate totala" putem observa un cateva
                              detalii in plus, sub forma: x / y. X reprezinta cantitatea totala a produsului respectiv, in timp ce y reprezinta cantitatea minima pe care acel produs
                              ar trebui sa o aiba. La implementarea aplicatiei, valorile y sunt egale cu 0, deoarece aceste cantitati minime sunt setate de catre userii ce opereaza
                              pe aplicatie, avand flexibilitatea necesara de a face acest lucru. Cand un produs are cantitatea totala mai mica decat cantitatea minima (deci x < y),
                              formatul x / y va fi rosu, pentru a fi mai usor de observat cand un produs are in stoc mai putin decat ar trebui sa aiba.
                            </p> 
                            <p>Functionalitatea "cantitate minima" va fi explicata mai jos, la sectiunea specifica acesteia.
                            </p>     
