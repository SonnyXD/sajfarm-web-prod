                        <div class="card-body">
                            <h3 class="mb-4">Operatiuni</h3>
                            <p>Sectiunea <b>OPERATIUNI</b> contine toate functionalitatile necesare.
                            </p>
                            <hr>
                            <h3 class="mb-4" id="intrare-factura">Intrare Factura</h3>
                            <p><b>Intrare Factura</b> este <b>unica</b> modalitate prin care user-ul poate sa introduca produse noi in aplicatie. O data selectata aceasta operatiune,
                          user-ul este redirectionat catre formularul pentru completarea datelor facturii pe care acesta vrea sa o introduca; date precum furnizorul, numarul facturii,
                          data scadenta etc.
                          </p>
                          <p>Dupa completarea datelor facturii, se vor introduce produsele ce apar pe factura. Exista un selector numit "Medicamente/Materiale Sanitare", unde pot fi
                            selectate produsele. Spre exemplu, daca vreau sa introduc Algocalmin 500mg, apas pe selector, caut produsul, si apas pe butonul "Adauga Pozitie".
                            Dupa apasarea acestui buton, un alt formular va aparea pe ecran, unde vom introduce datele produsului respectiv, date precum cantitate, termen de expirare,
                            lot etc. (Codul CIM este optional, deci nu se bifeaza casura "Cod CIM" daca produsul nu are acest cod).
                          </p>
                          <p>Toate campurile sunt obligatorii, in afara de codul CIM. Daca totusi exista cazuri in care produsul sa nu aiba lot spre exemplu, acesta se completeaza cu "0".
                        </p>
                        <p>O data ce completam formularul pentru detaliile produsului, apasam pe butonul "Adauga". In partea de jos a paginii vom putea observa un tabel de previzualizare
                          a produselor pe care le-am introdus pana acum pentru factura respectiva.
                        </p>
                        <p>Daca se constata faptul ca am introdus un produs cu detalii gresite, in tabelul de previzualizare exista butonul "Sterge" in dreptul fiecarei pozitii. Apasand pe el,
                          stergem pozitia respectiva si readaugam produsul cu datele corecte.
                        </p>
                        <p><b style="color: red;">ATENTIE!!!</b> Butonul "Adauga in Farmacie si genereaza NIR-ul" se apasa <b>NUMAI</b> dupa ce vor fi introduse toate produsele de pe factura,
                        deoarece odata introdusa gresit, greseala se repara cu greu. Odata apasat pe acest buton, in baza de date se vor salva produsele, vor aparea in gestiunea farmaciei si un
                        PDF cu NIR-ul se va genera automat cu toate calculele si datele necesare.
                        </p>
                        <hr>
                        <h3 class="mb-4" id="checklist-statii">Checklist Statii</h3>
                        <p><b>Checklist Statii</b> reprezinta actiunea prin care introducem checklist-urile pentru autosanitare.
                        </p>
                        <p><b>"De unde se face consumul"</b> - reprezinta gestiunea din care vor fi incarcate produsele din selectorul "Medicamente/Materiale Sanitare". Trebuie aleasa gestiunea
                        cu grija, deoarece se va face scaderea produselor adaugate in checklist in momentul in care acesta va fi generat. Celelalte informatii se explica de la sine, informatii
                        precum "Ambulanta", "Asistent" etc.
                      </p>
                      <p>Pentru aceasta operatiune este de asemenea implementat si tabelul de previzualizare a produselor pe care le-am adaugat in checklist, inainte de a-l finaliza. Odata ce
                        am completat checklist-ul cu datele necesare, se apasa butonul "Finalizeaza checklist-ul".
                      </p>
                      <hr>
                      <h3 class="mb-4" id="checklist-medici">Checklist Medici</h3>
                      <p><b>Checklist Medici</b> este operatiunea prin care introducem checklist-urile pentru medici. Functioneaza exact la fel ca operatiunea <b>Checklist Statii</b>, anume
                      trebuie completate datele din formular, inclusiv produsele.
                      </p>
                      <hr>
                      <h3 class="mb-4" id="bon-transfer">Bon Transfer</h3>
                      <p><b>Bon Transfer</b> este actiunea prin care efectuam transferul de produse dintre gestiuni. Se completeaza datele din formular, inclusiv produsele ce trebuie sa fie
                      transferate.
                    </p>
                    <hr>
                    <h3 class="mb-4" id="bon-consum-ambulante">Bon Consum Ambulante</h3>
                    <p> Pe pagina <b>Bon Consum Ambulante</b> putem genera bonurile de consum pentru fiecare autosanitara in parte. Se selecteaza substatia si autosanitara, iar in partea de jos
                    a paginii, putem observa un tabel cu toate checklist-urile ce apartin de autosanitara aleasa. Pentru a genera bonul de consum, se selecteaza perioada pentru consum; de obicei
                    se selecteaza luna intreaga, spre exemplu: 01-07-2022 - 31-07-2022. O data selectata perioada si apasat pe butonul "Genereaza Bonul de Consum", aplicatia va prelua toate checklist-urile
                    din perioada selectata pentru autosanitara aleasa si va crea bonul de consum, cu toate produsele din checklist-uri.
                    </p>
                    <hr>
                    <h3 class="mb-4" id="bon-consum-medici">Bon Consum Medici</h3>
                    <p><b>Bon Consum Medici</b> functioneaza exact la fel, avand acelasi mecanism ca bonul de consum al ambulantelor.
                    </p>
                    <hr>
                    <h3 class="mb-4" id="aviz-intrare">Aviz Intrare</h3>
                    <p><b>Aviz Intrare</b> functioneaza ca <b>Intrare Factura</b>; singura diferenta este ca operatiunea este destinata donatiilor sau sponsorizarilor.
                    </p>
                    <hr>
                    <h3 class="mb-4" id="checklist-retur">Checklist Retur</h3>
                    <p><b>Checklist Retur</b> este checklist-ul ce trebuie introdus pentru produsele ce sunt deteriorate, scoase din uz, expirate samd. Mecanismul este acelasi ca cel al celorlalte
                    checklist-uri.
                    </p>
                    <hr>
                    <h3 class="mb-4" id="retur">Retur</h3>
                    <p><b>Retur</b> este operatiunea prin care efectuam procesul verbal de retur pentru produsele ce sunt scoase din uz. Functioneaza ca si <b>Bon Consum Ambulante</b>, preluand toate
                    checklist-urile din perioada aleasa, depinzand de gestiunea selectata
                    </p>
                    <hr>
                    <h3 class="mb-4" id="modif-cant">Modificare Cantitati Minime</h3>
                    <p>Prin <b>Modificare Cantitati Minime</b> putem seta cantitatile minime fiecarui produs in parte; functionalitatea este explicata <a href="#home">mai sus</a>.
                    </p>
